#include <iostream>

using namespace std;

int main() {

    cout << "Deret 10 hingga 0: ";
    for (int i = 10; i >= 0; i--) {
        cout << i << " ";
    }
    cout << endl;

    cout << "Deret 9 hingga 1: ";
    for (int i = 9; i >= 1; i--) {
        cout << i << " ";
    }
    cout << endl;

    return 0;
}